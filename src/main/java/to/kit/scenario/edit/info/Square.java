package to.kit.scenario.edit.info;

/**
 * 正方形.<br/>
 * (Dimensionの代わり)
 * @author Hidetaka Sasai.
 */
public final class Square {
	/** 幅. */
	public final int width;
	/** 高さ. */
	public final int height;

	/**
	 * インスタンス生成.
	 * @param width 幅
	 * @param height 高さ
	 */
	public Square(final int width, final int height) {
		this.width = width;
		this.height = height;
	}
}
